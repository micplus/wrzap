// 创建新的logger替换包内的logger；
// 这个行为不是并发安全的(换logger时没写完)，可以在程序初始化的时候配置它
func ResetDefault(l *Logger) {
	logger = l
	Info = logger.Info
	Warn = logger.Warn
	Error = logger.Error
	DPanic = logger.DPanic
	Panic = logger.Panic
	Fatal = logger.Fatal
	Debug = logger.Debug
}

type Logger struct {
	// zap库保证并发安全
	l     *zap.Logger
	level Level
}

// 包级唯一
var logger = New(os.Stderr, InfoLevel)

func Default() *Logger {
	return logger
}

func New(writer io.Writer, level Level) *Logger {
	if writer == nil {
		panic("nil log writer")
	}
	cfg := zap.NewProductionConfig()
	core := zapcore.NewCore(
		zapcore.NewJSONEncoder(cfg.EncoderConfig),
		zapcore.AddSync(writer),
		zapcore.Level(level),
	)
	logger := &Logger{
		l:     zap.New(core),
		level: level,
	}
	return logger
}

func (l *Logger) Sync() error {
	return l.l.Sync()
}

func Sync() error {
	if logger != nil {
		return logger.Sync()
	}
	return nil
}
